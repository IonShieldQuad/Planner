package com.ionshield.planner.map;

public class NodeData {
    public int id;
    public double x;
    public double y;
    public String name;
    public String desc;

    public NodeData(int id, double x, double y, String name, String desc) {
        this.id = id;
        this.x = x;
        this.y = y;
        this.name = name;
        this.desc = desc;
    }
}
